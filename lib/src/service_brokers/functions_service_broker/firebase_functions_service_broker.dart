class FirebaseFunctionsServiceBroker extends FunctionsServiceInterface {
  //
  //
  //

  FirebaseFunctionsServiceBroker({
    required super.region,
    required super.projectId,
    required super.authServiceBroker,
  });

  //
  //
  //

  @override
  Uri functionsEndpointUrl(String functionId) {
    return Uri(
      scheme: 'https',
      host: '${this.region}-${this.projectId}.cloudfunctions.net',
      path: functionId,
    );
  }
}
